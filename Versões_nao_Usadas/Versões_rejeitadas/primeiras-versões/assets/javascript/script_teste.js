"use strict";

var vertexShaderSource = `#version 300 es

// an attribute is an input (in) to a vertex shader.
// It will receive data from a buffer
in vec4 a_position;
in vec4 a_color;

// A matrix to transform the positions by
uniform mat4 u_matrix;

// a varying the color to the fragment shader
out vec4 v_color;

// all shaders have a main function
void main() {
  // Multiply the position by the matrix.
  gl_Position = u_matrix * a_position;

  // Pass the color to the fragment shader.
  v_color = a_color;
}
`;

var fragmentShaderSource = `#version 300 es

precision highp float;

// the varied color passed from the vertex shader
in vec4 v_color;

// we need to declare an output for the fragment shader
out vec4 outColor;

void main() {
  outColor = v_color;
}
`;

let mutex = true;


function main() {
  // Get A WebGL context
  /** @type {HTMLCanvasElement} */
  var canvas = document.querySelector("#canvas0");
  var gl = canvas.getContext("webgl2");
  if (!gl) {
    return;
  }

  var program = webglUtils.createProgramFromSources(gl,
      [vertexShaderSource, fragmentShaderSource]);

  var positionAttributeLocation = gl.getAttribLocation(program, "a_position");
  var colorAttributeLocation = gl.getAttribLocation(program, "a_color");

  var matrixLocation = gl.getUniformLocation(program, "u_matrix");

  var positionBuffer = gl.createBuffer();

  var vao = gl.createVertexArray();

  gl.bindVertexArray(vao);

  gl.enableVertexAttribArray(positionAttributeLocation);

  gl.bindBuffer(gl.ARRAY_BUFFER, positionBuffer);
  setGeometry(gl);

  var size = 3;          // 3 components per iteration
  var type = gl.FLOAT;   // the data is 32bit floats
  var normalize = false; // don't normalize the data
  var stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next position
  var offset = 0;        // start at the beginning of the buffer
  gl.vertexAttribPointer(
      positionAttributeLocation, size, type, normalize, stride, offset);

  var colorBuffer = gl.createBuffer();
  gl.bindBuffer(gl.ARRAY_BUFFER, colorBuffer);
  setColors(gl);

  gl.enableVertexAttribArray(colorAttributeLocation);

  var size = 3;          // 3 components per iteration
  var type = gl.UNSIGNED_BYTE;   // the data is 8bit unsigned bytes
  var normalize = true;  // convert from 0-255 to 0.0-1.0
  var stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next color
  var offset = 0;        // start at the beginning of the buffer
  gl.vertexAttribPointer(
      colorAttributeLocation, size, type, normalize, stride, offset);



  //teste do outro canvas
  //------------------------------------------------


  var canvas1 = document.querySelector("#canvas1");
  var gl1 = canvas1.getContext("webgl2");
  if (!gl1) {
    return;
  }

  var program1 = webglUtils.createProgramFromSources(gl1,
      [vertexShaderSource, fragmentShaderSource]);

  var positionAttributeLocation1 = gl1.getAttribLocation(program1, "a_position");
  var colorAttributeLocation1 = gl1.getAttribLocation(program1, "a_color");

  var matrixLocation1 = gl1.getUniformLocation(program1, "u_matrix");

  var positionBuffer1 = gl1.createBuffer();

  var vao1 = gl1.createVertexArray();

  gl1.bindVertexArray(vao1);

  gl1.enableVertexAttribArray(positionAttributeLocation1);

  gl1.bindBuffer(gl1.ARRAY_BUFFER, positionBuffer1);
  setGeometry(gl1);

  var size = 3;          // 3 components per iteration
  var type = gl.FLOAT;   // the data is 32bit floats
  var normalize = false; // don't normalize the data
  var stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next position
  var offset = 0;        // start at the beginning of the buffer
  gl1.vertexAttribPointer(
      positionAttributeLocation, size, type, normalize, stride, offset);

  var colorBuffer1 = gl1.createBuffer();
  gl1.bindBuffer(gl1.ARRAY_BUFFER, colorBuffer1);
  setColors(gl1);

  gl1.enableVertexAttribArray(colorAttributeLocation1);

  var size = 3;          // 3 components per iteration
  var type = gl.UNSIGNED_BYTE;   // the data is 8bit unsigned bytes
  var normalize = true;  // convert from 0-255 to 0.0-1.0
  var stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next color
  var offset = 0;        // start at the beginning of the buffer
  gl1.vertexAttribPointer(
      colorAttributeLocation, size, type, normalize, stride, offset);



  //Fim teste
  //---------------------------------------------------

  function radToDeg(r) {
    return r * 180 / Math.PI;
  }

  function degToRad(d) {
    return d * Math.PI / 180;
  }

  var translation = [800, 250, 0];
  var rotation = [degToRad(20), degToRad(10), degToRad(0)];
  var scale = [1, 1, 1];

  drawScene();
  drawScene1();

  webglLessonsUI.setupSlider("#x",      {value: translation[0], slide: updatePosition(0), max: gl.canvas.width });
  webglLessonsUI.setupSlider("#y",      {value: translation[1], slide: updatePosition(1), max: gl.canvas.height});
  webglLessonsUI.setupSlider("#z",      {value: translation[2], slide: updatePosition(2), max: gl.canvas.height});
  webglLessonsUI.setupSlider("#angleX", {value: radToDeg(rotation[0]), slide: updateRotation(0), max: 360});
  webglLessonsUI.setupSlider("#angleY", {value: radToDeg(rotation[1]), slide: updateRotation(1), max: 360});
  webglLessonsUI.setupSlider("#angleZ", {value: radToDeg(rotation[2]), slide: updateRotation(2), max: 360});
  webglLessonsUI.setupSlider("#scaleX", {value: scale[0], slide: updateScale(0), min: -5, max: 5, step: 0.01, precision: 2});
  webglLessonsUI.setupSlider("#scaleY", {value: scale[1], slide: updateScale(1), min: -5, max: 5, step: 0.01, precision: 2});
  webglLessonsUI.setupSlider("#scaleZ", {value: scale[2], slide: updateScale(2), min: -5, max: 5, step: 0.01, precision: 2});

  function updatePosition(index) {
    return function(event, ui) {
      translation[index] = ui.value;
      mutex = false;
      drawScene();
    };
  }

  function updateRotation(index) {
    return function(event, ui) {
      var angleInDegrees = ui.value;
      var angleInRadians = degToRad(angleInDegrees);
      rotation[index] = angleInRadians;
      mutex = false;
      drawScene();
    };
  }

  function updateScale(index) {
    return function(event, ui) {
      scale[index] = ui.value;
      mutex = false;
      drawScene();
    };
  }


  function drawScene() {
    webglUtils.resizeCanvasToDisplaySize(gl.canvas);

    gl.viewport(0, 0, gl.canvas.width, gl.canvas.height);

    gl.clearColor(0, 0, 0, 0);
    gl.clear(gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);

    gl.enable(gl.DEPTH_TEST);

    gl.enable(gl.CULL_FACE);

    gl.useProgram(program);

    gl.bindVertexArray(vao);

    var matrix = m4.projection(gl.canvas.clientWidth, gl.canvas.clientHeight, 400);
    matrix = m4.translate(matrix, translation[0], translation[1], translation[2]);
    matrix = m4.xRotate(matrix, rotation[0]);
    matrix = m4.yRotate(matrix, rotation[1]);
    matrix = m4.zRotate(matrix, rotation[2]);
    matrix = m4.scale(matrix, scale[0], scale[1], scale[2]);

    gl.uniformMatrix4fv(matrixLocation, false, matrix);

    var primitiveType = gl.TRIANGLES;
    var offset = 0;
    var count = 16 * 6;
    gl.drawArrays(primitiveType, offset, count);

   if (mutex){
      rotation[1] += degToRad(2);
      matrix = m4.yRotate(matrix, rotation[1])
      requestAnimationFrame(drawScene);
    }
    mutex = true;
    
  }

  function drawScene1() {
    webglUtils.resizeCanvasToDisplaySize(gl1.canvas);

    gl1.viewport(0, 0, gl1.canvas.width, gl1.canvas.height);

    gl1.clearColor(0, 0, 0, 0);
    gl1.clear(gl1.COLOR_BUFFER_BIT | gl1.DEPTH_BUFFER_BIT);

    gl1.enable(gl1.DEPTH_TEST);

    gl1.enable(gl1.CULL_FACE);

    gl1.useProgram(program1);

    gl1.bindVertexArray(vao1);

    var matrix1 = m4.projection(gl1.canvas.clientWidth, gl1.canvas.clientHeight, 400);
    matrix1 = m4.translate(matrix1, translation[0], translation[1], translation[2]);
    matrix1 = m4.xRotate(matrix1, rotation[0]);
    matrix1 = m4.yRotate(matrix1, rotation[1]);
    matrix1 = m4.zRotate(matrix1, rotation[2]);
    matrix1 = m4.scale(matrix1, scale[0], scale[1], scale[2]);

    gl1.uniformMatrix4fv(matrixLocation1, false, matrix1);

    var primitiveType = gl.TRIANGLES;
    var offset = 0;
    var count = 16 * 6;
    gl1.drawArrays(primitiveType, offset, count);

    if (mutex){
      matrix1 = m4.yRotate(matrix1, rotation[1]);
      requestAnimationFrame(drawScene1);
    }
    mutex = true;
    
  }
}

